    <p class="sec-text">Benteng Belgica pada awalnya adalah sebuah benteng yang dibangun oleh bangsa Portugis pada abad 16 di Pulau Naira, Maluku. Lama setelah itu, di lokasi benteng Portugis tersebut kemudian dibangun kembali sebuah benteng oleh VOC atas perintah Gubernur Jendral Pieter Both pada tanggal 4 September 1611. Benteng tersebut kemudian diberi nama Fort Belgica, sehingga pada saat itu, terdapat dua buah benteng di Pulau Neira yaitu; Benteng Belgica dan Benteng Nassau. Benteng ini dibangun dengan tujuan untuk menghadapi perlawanan masyarakat Banda yang menentang monopoli perdagangan pala oleh VOC.</p>
