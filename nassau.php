    <p class="first-heading">Benteng Nassau</p>
    <p class="sec-text">Benteng Nassau adalah benteng Belanda pertama yang dibangun di Pulau Neira, Kepulauan Banda, Maluku. Benteng ini selesai dibangun pada tahun 1609, tujuannya adalah untuk mengontrol perdagangan pala, yang pada saat itu hanya tumbuh di Kepulauan Banda.

      Portugis sebelumnya telah berusaha untuk membangun sebuah benteng di lokasi ini pada tahun 1529, tetapi setelah membangun fondasi, mereka meninggalkan pekerjaan karena adanya resistansi dari penduduk setempat. Pada 25 April 1609, komandan armada Belanda, Laksamana Verhoeven, mengirim 750 tentara untuk memulai membuat benteng di Maluku, kemudian mereka memilih bekas fondasi bangunan benteng yang telah dibuat oleh Portugis sebelumnya. Terancam oleh benteng dan kehadiran kekuatan Belanda, juga menentang rencana Belanda untuk memonopoli industri pala, maka orang Banda menyerang Belanda dan membunuh Laksamana Verhoeven dan 40 anak buahnya.
      
      Belajar dari kejadian tersebut, Belanda mempercepat pembangunan benteng dan akhirnya selesai pada tahun 1609. Selain menjadi tempat pertahanan, benteng ini juga menjadi kantor administrasi Belanda di Pulau Banda. Pembangunan benteng lainnya menyusul di Banda Besar antara lain Benteng Hollandia dan Benteng Belgica.</p>
    <p class="sec-text">Hingga saat ini, meski terkesan usang, Benteng Belgica masih berdiri kokoh dengan pesona dan aura megah yang tak pernah luntur. Ratusan tahun berselang, tepatnya tahun 2015, Benteng Belgica telah resmi terdaftar sebagai salah satu Cagar Budaya yang menjadi bagian dari jejak-jejak perdagangan rempah dunia masa lampau. Kini, Benteng Belgica menjadi salah satu destinasi pariwisata di Banda Neira yang populer dan memikat wisatawan lokal maupun asing.</p>
    <p class="sec-text">Benteng Nassau yang terletak antara Pulau Banda dan Banda Besar, merupakan benteng dengan ukuran besar pada masanya, tetapi kemegahannya kalah dengan kehadiran benteng lain yaitu Belgica. Kedua benteng ini masih dapat dilihat sampai sekarang.</p>
